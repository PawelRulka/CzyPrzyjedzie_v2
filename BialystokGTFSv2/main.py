async def run_loop() -> None:
    """
    Pełna pętla produkcyjna:
      1. Uruchom serwer HTTP GTFS-Static.
      2. Wygeneruj GTFS-Static dla wszystkich miast (pierwsze uruchomienie).
      3. Dla miast z realtime=True: uruchom wątek GTFS-RT (pliki .pb są udostępniane w tym samym serwerze HTTP).
      4. Codziennie o północy: ponownie generuj GTFS-Static; bloki są
         przebudowywane automatycznie z nowego ZIP wewnątrz load_from_zip.
    """
    # Inicjalizacja RealtimeState dla miast z realtime=True
    global _rt_states
    rt_dirs: dict[str, str] = {}
    for cfg in CITIES:
        if cfg.get("realtime"):
            city_name = cfg["name"]
            state     = RealtimeState(cfg)
            _rt_states[city_name]  = state
            rt_dirs[city_name]     = rt_output_dir(city_name)

    # Udostępniamy foldery RT do handlera HTTP (pliki .pb)
    global _rt_dirs_for_http
    _rt_dirs_for_http = rt_dirs

    # Serwery HTTP
    await start_static_http_server()

    # Pierwsze generowanie statyczne (ładuje też dane do RealtimeState)
    await run_all()

    # Uruchamiamy wątki GTFS-RT (startują po załadowaniu staticu)
    for cfg in CITIES:
        if cfg.get("realtime"):
            city_name = cfg["name"]
            state     = _rt_states[city_name]
            out_dir   = rt_dirs[city_name]
            t = threading.Thread(
                target=realtime_worker,
                args=(cfg, state, out_dir),
                daemon=True,
                name=f"rt-{city_name}",
            )
            t.start()
            log.info("[%s] Wątek GTFS-RT uruchomiony", city_name)

    # Codzienna pętla regeneracji statycznej o północy
    while True:
        now      = datetime.now(tz=_WARSAW)
        midnight = (now + timedelta(days=1)).replace(hour=0, minute=0, second=0, microsecond=0)
        wait_secs = (midnight - now).total_seconds()
        log.info(
            "Następna regeneracja GTFS-Static o północy (za %.0f s / %.1f h)",
            wait_secs, wait_secs / 3600,
        )
        await asyncio.sleep(wait_secs)

        # Regeneracja statyczna – load_from_zip wewnątrz run_city
        # automatycznie przebudowuje bloki z nowego ZIP.
        await run_all()


