def _haversine(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    R = 6_371_000
    phi1, phi2 = math.radians(lat1), math.radians(lat2)
    dphi       = math.radians(lat2 - lat1)
    dlambda    = math.radians(lon2 - lon1)
    a = math.sin(dphi / 2) ** 2 + math.cos(phi1) * math.cos(phi2) * math.sin(dlambda / 2) ** 2
    return 2 * R * math.atan2(math.sqrt(a), math.sqrt(1 - a))


def _time_to_seconds(t: str) -> int:
    h, m, s = map(int, t.split(":"))
    return h * 3600 + m * 60 + s


def generate_realtime_feeds(city_cfg: dict, state: RealtimeState, output_dir: str) -> None:
    """
    Pobiera traces.json, buduje VehiclePositions + TripUpdates i zapisuje pliki
    .pb oraz .json do output_dir.
    """
    city_name  = city_cfg["name"]
    traces_url = city_cfg["cesip_traces_url"]

    # Pobranie traces
    try:
        r = requests.get(traces_url, timeout=10)
        r.raise_for_status()
        data = r.json()
        if not isinstance(data, dict):
            log.warning("[%s] traces.json – nieoczekiwany typ JSON", city_name)
            return
        traces = data.get("traces")
        if not isinstance(traces, list):
            log.warning("[%s] traces.json – brak pola 'traces'", city_name)
            return
    except Exception as exc:
        log.warning("[%s] Błąd pobierania traces.json: %s", city_name, exc)
        return

    log.info("[%s] traces count: %d", city_name, len(traces))

    now_ts = int(time.time())

    feed_vp = gtfs_realtime_pb2.FeedMessage()
    feed_tu = gtfs_realtime_pb2.FeedMessage()

    for feed in (feed_vp, feed_tu):
        feed.header.gtfs_realtime_version = "2.0"
        feed.header.incrementality        = gtfs_realtime_pb2.FeedHeader.FULL_DATASET
        feed.header.timestamp             = now_ts

    vp_json: dict = {"header": {"gtfs_realtime_version": "2.0", "timestamp": now_ts}, "entity": []}
    tu_json: list = []

    with state._lock:
        stops_snap      = state.stops
        stop_times_snap = state.stop_times

    for t in traces:
        brigade = t.get("brigade")
        index   = t.get("courseBrigadeIndex")
        trip_id = route_id = None

        if brigade is not None and index is not None:
            course = state.get_course(brigade, index)
            if course:
                trip_id  = course["trip_id"]
                route_id = course["route_id"]
                log.debug("[%s] trip_id=%s route_id=%s", city_name, trip_id, route_id)

        # ── VehiclePosition ───────────────────────────────────────────────
        e   = feed_vp.entity.add()
        e.id = f"VP_{t['vehicleNo']}"

        vp               = e.vehicle
        vp.vehicle.id    = str(t["vehicleNo"])
        vp.vehicle.label = str(t["vehicleNo"])
        if trip_id:
            vp.trip.trip_id = trip_id
        if route_id:
            vp.trip.route_id = route_id
        vp.position.latitude  = t["lat"]
        vp.position.longitude = t["lon"]
        vp.position.speed     = (t.get("speed") or 0) / 3.6
        vp.timestamp          = now_ts

        entity: dict = {
            "id":      str(t["vehicleNo"]),
            "vehicle": {
                "vehicle":  {"id": str(t["vehicleNo"]), "label": str(t["vehicleNo"])},
                "position": {"latitude": t["lat"], "longitude": t["lon"], "speed": t.get("speed") or 0},
                "timestamp": now_ts,
            },
        }
        if trip_id or route_id:
            entity["vehicle"]["trip"] = {}
            if trip_id:
                entity["vehicle"]["trip"]["trip_id"] = trip_id
            if route_id:
                entity["vehicle"]["trip"]["route_id"] = route_id
        vp_json["entity"].append(entity)

        if not trip_id:
            continue

        # ── TripUpdate ────────────────────────────────────────────────────
        stops_for_trip = stop_times_snap.get(str(trip_id))
        if not stops_for_trip:
            log.debug("[%s] Brak stop_times dla trip_id=%s", city_name, trip_id)
            continue

        next_stop  = stops_for_trip[0]
        stop_coords = stops_snap.get(next_stop["stop_id"])
        if stop_coords is None:
            continue
        stop_lat, stop_lon = stop_coords

        dist     = _haversine(t["lat"], t["lon"], stop_lat, stop_lon)
        now_dt   = datetime.now(tz=_WARSAW)
        now_sec  = now_dt.hour * 3600 + now_dt.minute * 60 + now_dt.second
        sched    = _hhmmss_to_secs(next_stop["arrival"])
        speed_ms = max((t.get("speed") or 10) / 3.6, 1)
        delay    = int(now_sec + dist / speed_ms - sched)

        log.debug("[%s] next_stop=%s dist=%dm delay=%ds", city_name, next_stop["stop_id"], int(dist), delay)

        e2    = feed_tu.entity.add()
        e2.id = f"TU_{trip_id}"

        tu              = e2.trip_update
        tu.trip.trip_id = trip_id
        tu.trip.route_id = route_id
        tu.timestamp    = now_ts

        stu               = tu.stop_time_update.add()
        stu.stop_id       = next_stop["stop_id"]
        stu.stop_sequence = next_stop["seq"]
        stu.arrival.delay   = delay
        stu.departure.delay = delay

        tu_json.append(trip_id)

    # Zapis plików
    os.makedirs(output_dir, exist_ok=True)
    for filename, content in (
        ("vehicle_positions.pb",   feed_vp.SerializeToString()),
        ("trip_updates.pb",        feed_tu.SerializeToString()),
        ("vehicle_positions.json", json.dumps(vp_json, indent=2, ensure_ascii=False).encode()),
        ("trip_updates.json",      json.dumps(tu_json, indent=2, ensure_ascii=False).encode()),
    ):
        with open(os.path.join(output_dir, filename), "wb") as fh:
            fh.write(content if isinstance(content, bytes) else content)

    log.info("[%s] GTFS-RT zapisane (%d pojazdów, %d trip_updates)", city_name, len(feed_vp.entity), len(feed_tu.entity))


