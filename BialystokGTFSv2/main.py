_WARSAW = ZoneInfo("Europe/Warsaw")


def secs_to_hhmmss(secs: int) -> str:
    h = secs // 3600
    m = (secs % 3600) // 60
    s = secs % 60
    return f"{h:02d}:{m:02d}:{s:02d}"


def _hhmmss_to_secs(t: str) -> int:
    """Konwertuje HH:MM:SS (dopuszczalne H > 23 dla kursów po północy) na sekundy."""
    h, m, s = map(int, t.split(":"))
    return h * 3600 + m * 60 + s


def ms_to_date_str(ms: int) -> str:
    return datetime.fromtimestamp(ms / 1000, tz=_WARSAW).strftime("%Y%m%d")


def strip_today_suffix(symbol: str) -> str:
    idx = symbol.find("_T")
    return symbol[:idx] if idx != -1 else symbol


def write_gtfs_csv(rows: list[dict], fieldnames: list[str]) -> str:
    buf = io.StringIO()
    writer = csv.DictWriter(buf, fieldnames=fieldnames, lineterminator="\r\n", extrasaction="ignore")
    writer.writeheader()
    writer.writerows(rows)
    return buf.getvalue()


def build_route_color_lookup(route_colors_cfg: list[dict]) -> dict[str, tuple[str, str]]:
    lookup: dict[str, tuple[str, str]] = {}
    for rule in route_colors_cfg:
        color      = rule["route_color"]
        text_color = rule["route_text_color"]
        spec       = rule["lines"]
        if isinstance(spec, list):
            names = [str(n) for n in spec]
        elif isinstance(spec, str) and "-" in spec:
            parts = spec.split("-", 1)
            if parts[0].isdigit() and parts[1].isdigit():
                names = [str(i) for i in range(int(parts[0]), int(parts[1]) + 1)]
            else:
                names = [spec]
        else:
            names = [str(spec)]
        for name in names:
            lookup[name] = (color, text_color)
    return lookup


def build_block_id_lookup(block_id_merges: list[dict]) -> dict[tuple[str, str], str]:
    lookup: dict[tuple[str, str], str] = {}
    for rule in block_id_merges:
        sid      = rule["service_id"]
        block_id = rule["block_id"]
        for brigade in rule["brigades"]:
            lookup[(sid, brigade)] = block_id
    return lookup


