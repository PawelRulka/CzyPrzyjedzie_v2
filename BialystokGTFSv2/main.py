# ══════════════════════════════════════════════════════════════════════════════
#  OSRM – GENEROWANIE SHAPE'ÓW DLA RĘCZNYCH KURSÓW
# ══════════════════════════════════════════════════════════════════════════════

def _osrm_route(
    stops_lookup: dict[str, tuple[float, float]],
    ordered_stop_ids: list[str],
    osrm_url: str,
) -> list[tuple[float, float]]:
    """
    Odpytuje OSRM o trasę przechodzącą przez wszystkie przystanki (w kolejności).
    Zwraca listę punktów (lat, lon) tworzących shape.
    Jeśli OSRM niedostępny, zwraca linię prostą między przystankami.
    """
    # Budujemy ciąg współrzędnych dla OSRM: lon,lat;lon,lat;...
    coords_parts: list[str] = []
    for sid in ordered_stop_ids:
        pos = stops_lookup.get(sid)
        if pos is None:
            raise ValueError(f"Przystanek '{sid}' nie istnieje w stops (sprawdź EXTRA_STOPS i dane API)")
        lat, lon = pos
        coords_parts.append(f"{lon},{lat}")

    coords_str = ";".join(coords_parts)
    url = f"{osrm_url.rstrip('/')}/route/v1/driving/{coords_str}"
    params = {"overview": "full", "geometries": "geojson", "steps": "false"}

    try:
        resp = requests.get(url, params=params, timeout=30)
        resp.raise_for_status()
        data = resp.json()
        if data.get("code") == "Ok" and data.get("routes"):
            geometry = data["routes"][0]["geometry"]["coordinates"]
            # OSRM zwraca [lon, lat] → odwracamy na (lat, lon)
            return [(c[1], c[0]) for c in geometry]
        log.warning("OSRM zwrócił błąd dla shape: %s", data.get("message", "?"))
    except Exception as exc:
        log.warning("Błąd połączenia z OSRM (%s): %s – używam linii prostej", osrm_url, exc)

    # Fallback: linia prosta między wszystkimi przystankami
    points: list[tuple[float, float]] = []
    for sid in ordered_stop_ids:
        pos = stops_lookup[sid]
        if not points or points[-1] != pos:
            points.append(pos)
    return points


def _build_shapes_rows_for_manual(
    shape_id: str,
    shape_points: list[tuple[float, float]],
    existing_seq_offset: int = 0,
) -> list[dict]:
    """Przekształca listę (lat, lon) na wiersze shapes.txt z narastającym dystansem."""
    rows: list[dict] = []
    total_dist = 0.0
    EPSILON    = 1e-6  # km – minimalny przyrost (zapobiega duplikatom shape_dist_traveled)

    for i, (lat, lon) in enumerate(shape_points):
        if i > 0:
            prev_lat, prev_lon = shape_points[i - 1]
            R     = 6371.0
            dlat  = math.radians(lat - prev_lat)
            dlon  = math.radians(lon - prev_lon)
            a     = (math.sin(dlat / 2) ** 2
                     + math.cos(math.radians(prev_lat))
                     * math.cos(math.radians(lat))
                     * math.sin(dlon / 2) ** 2)
            dist  = 2 * R * math.atan2(math.sqrt(a), math.sqrt(1 - a))
            total_dist += max(dist, EPSILON)

        rows.append({
            "shape_id":            shape_id,
            "shape_pt_lat":        f"{lat:.6f}",
            "shape_pt_lon":        f"{lon:.6f}",
            "shape_pt_sequence":   existing_seq_offset + i,
            "shape_dist_traveled": f"{total_dist:.3f}",
        })
    return rows


# ══════════════════════════════════════════════════════════════════════════════
#  WSTRZYKIWANIE RĘCZNYCH KURSÓW DO ZIP
# ══════════════════════════════════════════════════════════════════════════════

def inject_manual_trips(zip_bytes: bytes, city_name: str) -> bytes:
    """
    Wstrzykuje EXTRA_STOPS i MANUAL_TRIPS do GTFS ZIP wygenerowanego przez
    generate_city().

    Kolejność operacji jest gwarantowana przez wywołującego (run_city):
      1. generate_city()       – pobiera dane z API, usuwa nieużywane przystanki
                                 (cleanup), zwraca sprzątniętyi ZIP.
      2. inject_manual_trips() – dopiero tutaj dodaje EXTRA_STOPS i MANUAL_TRIPS
                                 do już oczyszczonego ZIP.

    Dzięki temu EXTRA_STOPS i MANUAL_TRIPS nie są widoczne podczas cleanup'u
    w generate_city() i nie mogą zostać przypadkowo odfiltrowane ani
    zanieczyszczać statystyk used_stop_ids.

    Zwraca bajty zmodyfikowanego ZIP.
    """
    if not EXTRA_STOPS and not MANUAL_TRIPS:
        return zip_bytes  # nic do roboty

    # ── 1. Wczytaj wszystkie pliki z ZIP do słownika nazwa→tekst ─────────────
    file_contents: dict[str, str] = {}
    with zipfile.ZipFile(io.BytesIO(zip_bytes)) as zf:
        for name in zf.namelist():
            with zf.open(name) as f:
                file_contents[name] = f.read().decode("utf-8")

    def _parse_csv(text: str) -> tuple[list[str], list[dict]]:
        """Zwraca (fieldnames, rows)."""
        reader = csv.DictReader(io.StringIO(text))
        rows   = list(reader)
        return list(reader.fieldnames or []), rows  # type: ignore[arg-type]

    # ── 2. Parsowanie plików potrzebnych do dalszej pracy ────────────────────
    stops_fields, stops_rows       = _parse_csv(file_contents["stops.txt"])
    trips_fields, trips_rows       = _parse_csv(file_contents["trips.txt"])
    st_fields,    st_rows          = _parse_csv(file_contents["stop_times.txt"])
    shapes_fields, shapes_rows     = _parse_csv(file_contents["shapes.txt"])
    _, cal_rows                    = _parse_csv(file_contents["calendar_dates.txt"])

    # Istniejące stop_id po cleanup'ie (tylko przystanki używane przez kursy z API)
    existing_stop_ids: set[str] = {r["stop_id"] for r in stops_rows}

    # Istniejące trip_id (żeby wykryć kolizje)
    existing_trip_ids: set[str] = {r["trip_id"] for r in trips_rows}

    # Mapa stop_id → (lat, lon) zbudowana z oczyszczonego stops.txt
    stops_lookup: dict[str, tuple[float, float]] = {
        r["stop_id"]: (float(r["stop_lat"]), float(r["stop_lon"]))
        for r in stops_rows
    }

    # Wszystkie service_id obecne w kalendarzu
    all_service_ids: set[str] = {r["service_id"] for r in cal_rows}

    log.info("[%s] inject_manual_trips: %d dodatkowych przystanków, %d ręcznych kursów",
             city_name, len(EXTRA_STOPS), len(MANUAL_TRIPS))

    # ── 3. EXTRA_STOPS → stops.txt ────────────────────────────────────────────
    # Dodajemy do już oczyszczonego stops.txt (po cleanup'ie z generate_city).
    added_stops = 0
    for es in EXTRA_STOPS:
        sid = str(es["stop_id"])
        if sid in existing_stop_ids:
            log.debug("[%s] Przystanek '%s' już istnieje – pomijam", city_name, sid)
            continue
        stops_rows.append({
            "stop_id":       sid,
            "stop_code":     str(es.get("stop_code", sid)),
            "stop_name":     es["stop_name"],
            "stop_lat":      str(es["stop_lat"]),
            "stop_lon":      str(es["stop_lon"]),
            "location_type": str(es.get("location_type", 0)),
        })
        stops_lookup[sid] = (float(es["stop_lat"]), float(es["stop_lon"]))
        existing_stop_ids.add(sid)
        added_stops += 1

    if added_stops:
        log.info("[%s] Dodano %d dodatkowych przystanków do stops.txt", city_name, added_stops)

    # ── 4. Shapes: ustal maksymalny istniejący numer sekwencji per shape_id ──
    # (potrzebne tylko jeśli w przyszłości shape_id już istnieje w shapes.txt;
    #  dla ręcznych kursów używamy prefiksu "M_" który nie koliduje z API)
    shape_seq_max: dict[str, int] = defaultdict(int)
    for sr in shapes_rows:
        sid = sr["shape_id"]
        seq = int(sr.get("shape_pt_sequence", 0))
        if seq > shape_seq_max[sid]:
            shape_seq_max[sid] = seq

    # Cache już wygenerowanych shape'ów (żeby nie odpytywać OSRM wielokrotnie
    # dla tego samego zestawu przystanków)
    _shape_cache: dict[str, list[tuple[float, float]]] = {}

    def _get_or_build_shape(stop_ids: list[str], shape_id: str) -> None:
        """Generuje shape przez OSRM (raz na unikalny shape_id) i dopisuje do shapes_rows."""
        if shape_id in _shape_cache:
            return
        log.info("[%s] Generowanie shape OSRM: %s (%d przystanków)", city_name, shape_id, len(stop_ids))
        pts = _osrm_route(stops_lookup, stop_ids, OSRM_URL)
        _shape_cache[shape_id] = pts
        new_rows = _build_shapes_rows_for_manual(shape_id, pts, existing_seq_offset=0)
        shapes_rows.extend(new_rows)
        log.info("[%s] Shape '%s': %d punktów", city_name, shape_id, len(new_rows))

    # ── 5. MANUAL_TRIPS → trips.txt + stop_times.txt + shapes.txt ────────────
    added_trips = 0
    skipped_trips = 0

    for mt in MANUAL_TRIPS:
        route_id     = str(mt["route_id"])
        block_id     = str(mt["block_id"])
        trip_headsign = mt.get("trip_headsign", "")
        direction_id  = str(mt.get("direction_id", ""))
        mt_service_ids: list[str] = [str(s) for s in mt.get("service_ids", [])]
        mt_stops: list[dict]      = mt["stops"]

        if len(mt_stops) < 2:
            log.warning("[%s] Ręczny kurs '%s' ma < 2 przystanki – pomijam", city_name, trip_headsign)
            skipped_trips += 1
            continue

        # Weryfikacja przystanków – stops_lookup zawiera już EXTRA_STOPS
        # dodane w kroku 3, więc referencje do nich są poprawnie rozwiązywane.
        stop_ids_ordered = [str(s["stop_id"]) for s in mt_stops]
        missing = [sid for sid in stop_ids_ordered if sid not in stops_lookup]
        if missing:
            log.warning(
                "[%s] Ręczny kurs '%s': brakujące stop_id %s – pomijam",
                city_name, trip_headsign, missing,
            )
            skipped_trips += 1
            continue

        # Shape_id oparty na liście przystanków (unikalny dla trasy, nie dla serwisu)
        shape_id = "M_" + "_".join(stop_ids_ordered)
        _get_or_build_shape(stop_ids_ordered, shape_id)

        for base_sid in mt_service_ids:
            # Sprawdź, czy w kalendarzu istnieje przynajmniej jeden wpis
            # pasujący do tego bazowego service_id (porównanie prefiksu)
            matching_service_ids = [
                cal_sid for cal_sid in all_service_ids
                if cal_sid == base_sid or cal_sid.startswith(base_sid + "-")
            ]
            if not matching_service_ids:
                log.warning(
                    "[%s] Ręczny kurs '%s': service_id '%s' nie znaleziony w calendar_dates – pomijam",
                    city_name, trip_headsign, base_sid,
                )
                skipped_trips += 1
                continue

            # Jeden trip per (kurs, service_id); używamy pierwszego pasującego
            # (w GTFS-Static jeden service_id = jeden typ dnia, daty są w calendar_dates)
            service_id_to_use = matching_service_ids[0]

            # Unikalne trip_id: prefiks "M", route, block, service, czas odjazdu
            dep_tag  = mt_stops[0]["departure_time"].replace(":", "")
            trip_id  = f"M_{route_id}_{block_id}_{service_id_to_use}_{dep_tag}"
            suffix   = 1
            base_tid = trip_id
            while trip_id in existing_trip_ids:
                trip_id = f"{base_tid}_{suffix}"
                suffix += 1
            existing_trip_ids.add(trip_id)

            # trips.txt
            trips_rows.append({
                "route_id":      route_id,
                "service_id":    service_id_to_use,
                "trip_id":       trip_id,
                "trip_headsign": trip_headsign,
                "direction_id":  direction_id,
                "shape_id":      shape_id,
                "block_id":      block_id,
            })

            # stop_times.txt
            for seq, stop_entry in enumerate(mt_stops):
                st_rows.append({
                    "trip_id":        trip_id,
                    "arrival_time":   stop_entry["departure_time"],
                    "departure_time": stop_entry["departure_time"],
                    "stop_id":        str(stop_entry["stop_id"]),
                    "stop_sequence":  seq,
                    "pickup_type":    str(stop_entry.get("pickup_type", 0)),
                    "drop_off_type":  str(stop_entry.get("drop_off_type", 0)),
                })

            added_trips += 1
            log.info(
                "[%s] Dodano ręczny kurs: trip_id='%s' route='%s' service='%s' headsign='%s'",
                city_name, trip_id, route_id, service_id_to_use, trip_headsign,
            )

    if skipped_trips:
        log.warning("[%s] Pominięto %d ręcznych kursów (patrz logi powyżej)", city_name, skipped_trips)

    log.info("[%s] inject_manual_trips: +%d kursów, +%d przystanków", city_name, added_trips, added_stops)

    # ── 6. Serializacja zmodyfikowanych plików i przepakowanie ZIP ────────────

    # Upewnij się, że shapes.txt ma kolumnę shape_dist_traveled
    shapes_out_fields = ["shape_id", "shape_pt_lat", "shape_pt_lon",
                         "shape_pt_sequence", "shape_dist_traveled"]

    # Istniejące wiersze shapes mogą nie mieć shape_dist_traveled – uzupełniamy ""
    for sr in shapes_rows:
        sr.setdefault("shape_dist_traveled", "")

    updated: dict[str, str] = {
        "stops.txt":      write_gtfs_csv(stops_rows,   stops_fields),
        "trips.txt":      write_gtfs_csv(trips_rows,   trips_fields),
        "stop_times.txt": write_gtfs_csv(st_rows,      st_fields),
        "shapes.txt":     write_gtfs_csv(shapes_rows,  shapes_out_fields),
    }
    file_contents.update(updated)

    # Złóż nowy ZIP
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        for name, text in file_contents.items():
            zf.writestr(name, text)

    size = buf.tell()
    log.info("[%s] ZIP po wstrzyknięciu: %.1f MB", city_name, size / 1_048_576)
    return buf.getvalue()


