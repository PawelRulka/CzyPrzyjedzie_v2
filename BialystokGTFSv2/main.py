async def handle_static_file(request: web.Request) -> web.Response:
    filename = request.match_info["filename"]
    # 1) pliki static: gtfs_output/<city>.zip
    static_path = os.path.join(OUTPUT_DIR, filename)
    if os.path.isfile(static_path):
        with open(static_path, "rb") as fh:
            data = fh.read()
        content_type = "application/zip" if filename.endswith(".zip") else "application/octet-stream"
        return web.Response(
            body=data,
            content_type=content_type,
            headers={"Content-Disposition": f'attachment; filename="{filename}"'},
        )

    # 2) pliki realtime: gtfs_output/rt_<city>/*.pb
    if filename in _ALLOWED_RT_FILES:
        rt_path = None
        for city_dir in _rt_dirs_for_http.values():
            candidate = os.path.join(city_dir, filename)
            if os.path.isfile(candidate):
                rt_path = candidate
                break

        if rt_path is None:
            raise web.HTTPServiceUnavailable(reason="Plik nie został jeszcze wygenerowany")

        with open(rt_path, "rb") as fh:
            data = fh.read()
        content_type = _ALLOWED_RT_FILES[filename]
        return web.Response(
            body=data,
            content_type=content_type,
            headers={"Content-Disposition": f'attachment; filename="{filename}"'},
        )

    raise web.HTTPNotFound(reason=f"Plik nie znaleziony: {filename}")


async def handle_static_index(request: web.Request) -> web.Response:
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    base  = f"http://{HTTP_HOST}:{HTTP_PORT}{HTTP_PREFIX}"
    zip_files: list[str] = sorted([f for f in os.listdir(OUTPUT_DIR) if f.endswith(".zip")])

    pb_files_set: set[str] = set()
    for city_dir in _rt_dirs_for_http.values():
        try:
            for f in os.listdir(city_dir):
                if f.endswith(".pb"):
                    pb_files_set.add(f)
        except FileNotFoundError:
            # RT katalog może jeszcze nie istnieć (generowanie w tle)
            pass

    all_files = sorted(set(zip_files) | pb_files_set)
    lines = [
        "<html><body><h2>GTFS Feed Server</h2><ul>",
        *[f'<li><a href="{base}/{f}">{f}</a></li>' for f in all_files],
        "</ul></body></html>",
    ]
    return web.Response(text="\n".join(lines), content_type="text/html")


async def start_static_http_server() -> None:
    app = web.Application()
    app.router.add_get(HTTP_PREFIX,              handle_static_index)
    app.router.add_get(HTTP_PREFIX + "/",        handle_static_index)
    app.router.add_get(HTTP_PREFIX + "/{filename}", handle_static_file)

    runner = web.AppRunner(app)
    await runner.setup()
    site = web.TCPSite(runner, HTTP_HOST, HTTP_PORT)
    await site.start()

    log.info("Serwer GTFS (Static+Realtime): http://%s:%d%s/", HTTP_HOST, HTTP_PORT, HTTP_PREFIX)
    for cfg in CITIES:
        name = cfg["name"]
        log.info("  Static  : http://%s:%d%s/%s.zip", HTTP_HOST, HTTP_PORT, HTTP_PREFIX, name)

    for fname in sorted([k for k in _ALLOWED_RT_FILES.keys() if k.endswith(".pb")]):
        log.info("  RT PB    : http://%s:%d%s/%s", HTTP_HOST, HTTP_PORT, HTTP_PREFIX, fname)


