class APIClient:
    def __init__(self, base_url: str, session: aiohttp.ClientSession, semaphore: asyncio.Semaphore) -> None:
        self.base    = base_url.rstrip("/")
        self.session = session
        self.sem     = semaphore

    async def get(self, path: str, params: dict | None = None) -> Any | None:
        url = f"{self.base}/{path}"
        async with self.sem:
            await asyncio.sleep(REQUEST_DELAY)
            for attempt in range(MAX_RETRIES):
                try:
                    async with self.session.get(
                        url, params=params, ssl=False,
                        timeout=aiohttp.ClientTimeout(total=30),
                    ) as resp:
                        resp.raise_for_status()
                        return await resp.json(content_type=None)
                except Exception as exc:
                    if attempt == MAX_RETRIES - 1:
                        log.warning("Request failed after %d attempts: %s %s - %s", MAX_RETRIES, url, params, exc)
                        return None
                    await asyncio.sleep(2 ** attempt)
        return None


