class RealtimeState:
    """
    Przechowuje dane GTFS-Static załadowane do pamięci RAM – przystanki,
    stop_times oraz mapę bloków – potrzebne do generowania GTFS-RT.
    Dane bloków są budowane bezpośrednio z trips.txt w ZIP (bez zewnętrznego API).
    Jedna instancja na miasto z włączonym realtime=True.

    Struktura blocks_cache:
        block_id → lista kursów POSORTOWANA wg pierwszego odjazdu rosnąco,
                   każdy kurs: {"trip_id": str, "route_id": str, "first_dep_sec": int}
        Indeks na tej liście (courseBrigadeIndex z traces.json) wskazuje
        konkretny kurs w ramach bloku.
    """

    def __init__(self, city_cfg: dict) -> None:
        self.city_cfg  = city_cfg
        self.city_name = city_cfg["name"]

        # Dane statyczne w RAM
        self.stops:      dict[str, tuple[float, float]] = {}  # stop_id → (lat, lon)
        self.stop_times: dict[str, list[dict]]          = {}  # trip_id → lista

        # Dzisiejszy bazowy service_id (np. "P", "R", "S")
        self.today_service_id: str | None = None

        # Mapa bloków zbudowana z trips.txt + stop_times.txt
        # block_id → [{"trip_id": str, "route_id": str, "first_dep_sec": int}, ...]
        # (posortowana wg first_dep_sec)
        self.blocks_cache: dict[str, list[dict]] = {}

        # Blokada do bezpiecznego odczytu/zapisu między wątkami
        self._lock = threading.Lock()

    # ── Ładowanie wszystkich danych z ZIP ─────────────────────────────────────

    def load_from_zip(self, zip_bytes: bytes) -> None:
        """
        Wczytuje z bajtów ZIP:
          • stops.txt          → self.stops
          • stop_times.txt     → self.stop_times
          • trips.txt          → self.blocks_cache  (przez _build_blocks)
          • calendar_dates.txt → self.today_service_id
        Wszystkie dane są przeładowywane atomowo pod blokadą.
        """
        city_name = self.city_name

        new_stops:      dict[str, tuple[float, float]] = {}
        new_stop_times: dict[str, list[dict]]          = {}
        # trip_id → {route_id, block_id} – potrzebne do zbudowania bloków
        trip_meta:      dict[str, dict]                = {}
        # Zbieramy pierwsze odjazdy per trip_id z stop_times (seq == min)
        # Używamy sekund od północy żeby można było sortować
        trip_first_dep: dict[str, int]                 = {}

        with zipfile.ZipFile(io.BytesIO(zip_bytes)) as zf:

            # stops.txt
            with zf.open("stops.txt") as f:
                for row in csv.DictReader(io.TextIOWrapper(f, encoding="utf-8")):
                    new_stops[row["stop_id"]] = (
                        float(row["stop_lat"]),
                        float(row["stop_lon"]),
                    )

            # trips.txt → zapamiętujemy route_id, block_id i service_id per trip_id
            with zf.open("trips.txt") as f:
                for row in csv.DictReader(io.TextIOWrapper(f, encoding="utf-8")):
                    trip_meta[row["trip_id"]] = {
                        "route_id":   row["route_id"],
                        "block_id":   row.get("block_id", ""),
                        "service_id": row.get("service_id", ""),
                    }

            # stop_times.txt → budujemy listę przystanków i wyznaczamy
            # czas pierwszego odjazdu (min stop_sequence) per trip_id
            with zf.open("stop_times.txt") as f:
                for row in csv.DictReader(io.TextIOWrapper(f, encoding="utf-8")):
                    tid = row["trip_id"]
                    seq = int(row["stop_sequence"])
                    dep = row["departure_time"]

                    new_stop_times.setdefault(tid, []).append({
                        "stop_id":   row["stop_id"],
                        "arrival":   row["arrival_time"],
                        "departure": dep,
                        "seq":       seq,
                    })

                    # Wyznaczamy czas pierwszego odjazdu (najniższy seq)
                    dep_sec = _hhmmss_to_secs(dep)
                    if tid not in trip_first_dep or seq < new_stop_times[tid][-2]["seq"]:
                        # Aktualizujemy bezpiecznie po sortowaniu poniżej
                        pass  # obsłużone po pętli

            # Sortowanie stop_times i wyznaczenie first_dep_sec
            for tid, stops_list in new_stop_times.items():
                stops_list.sort(key=lambda r: r["seq"])
                trip_first_dep[tid] = _hhmmss_to_secs(stops_list[0]["departure"])

            # calendar_dates.txt → dzisiejszy service_id
            today_str      = datetime.now(tz=_WARSAW).strftime("%Y%m%d")
            new_service_id: str | None = None
            with zf.open("calendar_dates.txt") as f:
                for row in csv.DictReader(io.TextIOWrapper(f, encoding="utf-8")):
                    if row["date"] == today_str and row["exception_type"] == "1":
                        new_service_id = row["service_id"].split("-")[0]
                        break

        # Budowanie blocks_cache z trip_meta i trip_first_dep
        new_blocks = self._build_blocks(
            trip_meta, trip_first_dep, new_service_id,
        )

        with self._lock:
            self.stops            = new_stops
            self.stop_times       = new_stop_times
            self.today_service_id = new_service_id
            self.blocks_cache     = new_blocks

        log.info(
            "[%s] RealtimeState załadowany: %d przystanków, %d kursów, "
            "%d bloków, service_id='%s'",
            city_name, len(new_stops), len(new_stop_times),
            len(new_blocks), new_service_id,
        )

    # ── Budowanie mapy bloków z danych GTFS ──────────────────────────────────

    def _build_blocks(
        self,
        trip_meta:      dict[str, dict],
        trip_first_dep: dict[str, int],
        today_service_id: str | None,
    ) -> dict[str, list[dict]]:
        """
        Buduje słownik block_id → lista kursów posortowana wg czasu
        pierwszego odjazdu.

        Uwzględnia wyłącznie kursy pasujące do dzisiejszego service_id
        (porównanie prefiksu, np. "P" pasuje do "P", "P-0", "P-2").
        Kursy bez block_id lub z pustym block_id są pomijane – nie da się
        ich przypisać do brygady.
        """
        raw: dict[str, list[dict]] = defaultdict(list)

        for tid, meta in trip_meta.items():
            block_id = meta.get("block_id", "").strip()
            if not block_id:
                continue  # kurs bez bloku – nie można dopasować przez indeks

            # Filtrowanie po dzisiejszym service_id (jeśli znany)
            # trip_meta nie zawiera service_id bezpośrednio – musimy go wyciągnąć;
            # jest przechowywany osobno dopiero po wczytaniu calendar_dates.
            # Zamiast tego filtrujemy po prefiksie service_id zawartym w trip_id.
            # trips.txt zawiera kolumnę service_id – wczytujemy go w load_from_zip
            # jako trip_meta["service_id"]. Dodamy go poniżej w load_from_zip.
            trip_service = meta.get("service_id", "")
            if today_service_id is not None:
                base = trip_service.split("-")[0]
                if base != today_service_id:
                    continue

            raw[block_id].append({
                "trip_id":       tid,
                "route_id":      meta["route_id"],
                "first_dep_sec": trip_first_dep.get(tid, 0),
            })

        # Sortowanie kursów w każdym bloku wg czasu pierwszego odjazdu
        result: dict[str, list[dict]] = {}
        for block_id, courses in raw.items():
            result[block_id] = sorted(courses, key=lambda c: c["first_dep_sec"])

        return result

    # ── Rozwiązywanie block_id (scalanie brygad) ──────────────────────────────

    def resolve_block_id(self, brigade: str) -> str:
        """
        Zwraca efektywny block_id uwzględniając reguły scalania brygad
        (BLOCK_ID_MERGES) dla dzisiejszego service_id.
        Jeśli brygada nie należy do żadnej grupy scalania, zwraca ją bez zmian
        – bo w trips.txt block_id == oryginalna brygada dla takich kursów.
        """
        sid = self.today_service_id
        if sid is None:
            return brigade
        for merge in (self.city_cfg.get("block_id_merges") or []):
            if merge["service_id"] == sid and brigade in merge["brigades"]:
                merged = merge["block_id"]
                log.debug("[%s] Brygada '%s' → block_id '%s'", self.city_name, brigade, merged)
                return merged
        return brigade

    # ── Pobieranie kursu po brygadzie i indeksie ─────────────────────────────

    def get_course(self, brigade, index: int) -> dict | None:
        """
        Zwraca kurs dla danej brygady i indeksu (courseBrigadeIndex z traces).
        Indeks odpowiada pozycji na posortowanej wg czasu liście kursów w bloku.
        """
        brigade_str = str(brigade)
        block_id    = self.resolve_block_id(brigade_str)

        with self._lock:
            courses = self.blocks_cache.get(block_id)
            # Fallback: jeśli scalony block_id nie istnieje, próbuj oryginalnej brygady
            if not courses and block_id != brigade_str:
                log.debug(
                    "[%s] block_id '%s' nie w cache – próba oryginalnej brygady '%s'",
                    self.city_name, block_id, brigade_str,
                )
                courses  = self.blocks_cache.get(brigade_str)
                block_id = brigade_str if courses else block_id

        if not courses:
            log.debug("[%s] Brygada '%s' (block_id='%s') nie w cache",
                      self.city_name, brigade_str, block_id)
            return None
        if index >= len(courses):
            log.debug(
                "[%s] courseBrigadeIndex=%d poza zakresem dla block_id='%s' (len=%d)",
                self.city_name, index, block_id, len(courses),
            )
            return None
        return courses[index]


