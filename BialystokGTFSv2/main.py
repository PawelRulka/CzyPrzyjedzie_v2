#  SERWER HTTP – GTFS-Realtime (synchroniczny BaseHTTPRequestHandler, port 8060)
# ══════════════════════════════════════════════════════════════════════════════

_ALLOWED_RT_FILES = {
    "vehicle_positions.pb":   "application/octet-stream",
    "trip_updates.pb":        "application/octet-stream",
    "vehicle_positions.json": "application/json",
    "trip_updates.json":      "application/json",
}


def _make_rt_handler(rt_dirs: dict[str, str]):
    """Fabryka handlera z zamkniętym rt_dirs (city_name → ścieżka katalogu)."""

    class RealtimeHandler(BaseHTTPRequestHandler):
        def do_GET(self):
            prefix = RT_HTTP_PREFIX + "/"
            if not self.path.startswith(prefix):
                self.send_error(404, "Not Found")
                return

            rest  = self.path[len(prefix):].split("?")[0]
            parts = rest.split("/", 1)
            if len(parts) != 2:
                self.send_error(404, "Oczekiwano: /<city>/<filename>")
                return

            city_name, filename = parts
            if filename not in _ALLOWED_RT_FILES:
                self.send_error(404, f"Nieznany plik: {filename}")
                return

            city_dir = rt_dirs.get(city_name)
            if city_dir is None:
                self.send_error(404, f"Nieznane miasto: {city_name}")
                return

            filepath = os.path.join(city_dir, filename)
            if not os.path.isfile(filepath):
                self.send_error(503, "Plik nie został jeszcze wygenerowany")
                return

            content_type = _ALLOWED_RT_FILES[filename]
            with open(filepath, "rb") as f:
                data = f.read()

            self.send_response(200)
            self.send_header("Content-Type", content_type)
            self.send_header("Content-Length", str(len(data)))
            self.end_headers()
            self.wfile.write(data)

        def log_message(self, fmt, *args):
            pass  # wyciszamy logi HTTP

    return RealtimeHandler


def start_realtime_http_server(rt_dirs: dict[str, str]) -> None:
    handler = _make_rt_handler(rt_dirs)
    server  = HTTPServer(("127.0.0.1", RT_HTTP_PORT), handler)
    log.info(
        "Serwer GTFS-RT   : http://127.0.0.1:%d%s/<miasto>/<plik>",
        RT_HTTP_PORT, RT_HTTP_PREFIX,
    )
    for city_name in rt_dirs:
        for fname in ("vehicle_positions.pb", "trip_updates.pb"):
            log.info(
                "  RT: http://127.0.0.1:%d%s/%s/%s",
                RT_HTTP_PORT, RT_HTTP_PREFIX, city_name, fname,
            )
    threading.Thread(target=server.serve_forever, daemon=True).start()


