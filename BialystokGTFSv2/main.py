def main() -> None:
    parser = argparse.ArgumentParser(
        description="GTFS-Static + GTFS-Realtime generator (przystanki.*)",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Przykłady:\n"
            "  python gtfs_combined.py               # wszystkie miasta, pętla dzienna + serwery HTTP\n"
            "  python gtfs_combined.py bialystok     # jednorazowe generowanie static dla jednego miasta\n"
        ),
    )
    parser.add_argument(
        "city",
        nargs="?",
        help=(
            "Nazwa miasta do jednorazowego generowania GTFS-Static "
            "(bez serwera HTTP i bez pętli realtime). "
            "Pomiń, by uruchomić wszystkie miasta w trybie ciągłym."
        ),
    )
    args = parser.parse_args()

    if args.city:
        cfg = next((c for c in CITIES if c["name"] == args.city), None)
        if cfg is None:
            log.error("Nieznane miasto '%s'. Dostępne: %s",
                      args.city, ", ".join(c["name"] for c in CITIES))
            sys.exit(1)
        asyncio.run(run_city(cfg))
    else:
        try:
            asyncio.run(run_loop())
        except KeyboardInterrupt:
            log.info("Przerwano – zamykanie.")


if __name__ == "__main__":
    main()