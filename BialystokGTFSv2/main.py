async def generate_city(city_cfg: dict) -> bytes:
    """
    Pobiera dane API dla jednego miasta i zwraca GTFS ZIP jako bajty.

    Zwrócony ZIP zawiera WYŁĄCZNIE dane z endpointów API – bez EXTRA_STOPS
    i MANUAL_TRIPS. Ich wstrzyknięcie jest celowo odroczone do inject_manual_trips(),
    która musi być wywołana na wyniku tej funkcji. Dzięki temu cleanup
    nieużywanych przystanków (filtr used_stop_ids) obejmuje tylko dane z API
    i nie jest zanieczyszczony ręcznie dodanymi pozycjami.
    """

    city_name  = city_cfg["name"]
    base_url   = city_cfg["base_url"]
    agency_cfg = city_cfg["agency"]
    feed_cfg   = city_cfg["feed_info"]

    route_color_lookup = build_route_color_lookup(city_cfg.get("route_colors") or [])
    block_id_lookup    = build_block_id_lookup(city_cfg.get("block_id_merges") or [])
    use_route_colors   = bool(route_color_lookup)

    now          = datetime.now()
    feed_start   = now.strftime("%Y%m%d")
    feed_end     = (now + timedelta(days=90)).strftime("%Y%m%d")
    feed_version = now.strftime("%Y%m%d%H%M")

    log.info("[%s] Rozpoczynanie generowania GTFS-Static ...", city_name)

    sem       = asyncio.Semaphore(CONCURRENCY)
    connector = aiohttp.TCPConnector(limit=CONCURRENCY)

    async with aiohttp.ClientSession(connector=connector) as session:
        api = APIClient(base_url, session, sem)

        # 1. Zasoby bazowe
        log.info("[%s] Pobieranie danych bazowych (przystanki, linie, kalendarz, warianty) ...", city_name)
        stops_data, lines_data, calendar_data, variant_names_raw, main_variants_raw = await asyncio.gather(
            api.get("getStops.json"),
            api.get("getLines.json"),
            api.get("getCalendar.json"),
            api.get("getMappedVariantNames.json"),
            api.get("getMainVariantsByLineName.json"),
        )

        stops         = stops_data["stopPoints"]
        lines         = lines_data["lines"]
        calendar_days = calendar_data["days"]
        variant_names = variant_names_raw or {}

        variant_direction: dict[int, int] = {}
        for _line, variants in (main_variants_raw or {}).items():
            if isinstance(variants, list):
                if len(variants) > 1 and variants[1] is not None:
                    variant_direction[int(variants[1])] = 0
                if len(variants) > 2 and variants[2] is not None:
                    variant_direction[int(variants[2])] = 1

        log.info("[%s] direction_id: %d wariantów zmapowanych", city_name, len(variant_direction))

        # stop_id = symbol przystanku (string) – spójne z całym feedem GTFS.
        # Klucz słownika to symbol (używany w zapytaniach atomowych),
        # wartość – pełny obiekt przystanku z API.
        symbol_to_stop:  dict[str, dict] = {s["symbol"]: s for s in stops}
        line_name_to_id: dict[str, int]  = {l["name"]: l["name"] for l in lines}

        log.info("[%s] %d przystanków, %d linii, %d dni kalendarza",
                 city_name, len(stops), len(lines), len(calendar_days))

        # 2. Kalendarz → service_id
        service_dates: dict[str, list[str]]    = defaultdict(list)
        service_representative: dict[str, int] = {}

        for day in calendar_days:
            raw_sym    = day["dayType"]["dayTypeSymbol"]
            service_id = strip_today_suffix(raw_sym)
            date_str   = ms_to_date_str(day["date"])
            service_dates[service_id].append(date_str)
            if "_T" not in raw_sym and service_id not in service_representative:
                service_representative[service_id] = day["date"]

        for day in calendar_days:
            raw_sym    = day["dayType"]["dayTypeSymbol"]
            service_id = strip_today_suffix(raw_sym)
            if service_id not in service_representative:
                service_representative[service_id] = day["date"]

        log.info("[%s] %d typy serwisu: %s",
                 city_name, len(service_representative), list(service_representative.keys()))

        # 3. Rozkłady atomowe
        total_calls = len(stops) * len(service_representative)
        log.info("[%s] Pobieranie rozkładów atomowych (%d przystanków x %d typów = %d zapytań) ...",
                 city_name, len(stops), len(service_representative), total_calls)

        async def fetch_atomic(stop_symbol: str, date_ms: int, service_id: str):
            data = await api.get("getAtomicSchedule.json", {"symbol": stop_symbol, "date": date_ms})
            return stop_symbol, service_id, data

        atomic_tasks = [
            fetch_atomic(s["symbol"], date_ms, sid)
            for sid, date_ms in service_representative.items()
            for s in stops
        ]

        trips_meta:     dict[int, dict]     = {}
        seen_orders:    dict[int, set]      = defaultdict(set)
        stop_times_raw: dict[int, list]     = defaultdict(list)

        completed      = 0
        atomic_results = await asyncio.gather(*atomic_tasks, return_exceptions=True)

        for result in atomic_results:
            completed += 1
            if completed % 500 == 0 or completed == total_calls:
                log.info("[%s] Przetworzono %d / %d odpowiedzi atomowych",
                         city_name, completed, total_calls)

            if isinstance(result, Exception) or result is None:
                continue

            stop_symbol, service_id, data = result
            if data is None or "lineSchedules" not in data:
                continue

            stop = symbol_to_stop.get(stop_symbol)
            if stop is None:
                continue

            # stop_id = symbol przystanku (string), spójny z getStops.json
            stop_id     = stop["symbol"]
            on_request  = bool(stop.get("onRequest"))
            getting_out = bool(stop.get("gettingOut"))

            if on_request:
                pickup_type, dropoff_type = 3, 3
            elif getting_out:
                pickup_type, dropoff_type = 1, 0
            else:
                pickup_type, dropoff_type = 0, 0

            for line_name, line_sched in data["lineSchedules"].items():
                route_id = line_name_to_id.get(line_name, line_name)
                for dep in line_sched["departures"]:
                    course_id  = dep["courseId"]
                    order      = dep["orderInCourse"]
                    variant_id = dep["variantId"]
                    brigade    = dep.get("brigade") or ""

                    if course_id not in trips_meta:
                        headsign = (
                            dep.get("optionalDirection")
                            or variant_names.get(str(variant_id), "")
                        )
                        trips_meta[course_id] = {
                            "trip_id":       course_id,
                            "route_id":      route_id,
                            "service_id":    service_id,
                            "shape_id":      variant_id,
                            "trip_headsign": headsign,
                            "variant_id":    variant_id,
                            "brigade":       brigade,
                        }

                    if order not in seen_orders[course_id]:
                        seen_orders[course_id].add(order)
                        stop_times_raw[course_id].append((
                            order, stop_id, dep["scheduledDepartureSec"],
                            pickup_type, dropoff_type,
                        ))

        log.info("[%s] Zebrano %d kursów, %d wpisów stop_times",
                 city_name, len(trips_meta),
                 sum(len(v) for v in stop_times_raw.values()))

        # 4. Shapes + nagłówki wariantów
        variant_ids = sorted({t["variant_id"] for t in trips_meta.values()})
        log.info("[%s] Pobieranie %d kształtów wariantów ...", city_name, len(variant_ids))

        shape_tasks   = [api.get("getTheoreticalCourseDetails.json", {"variantId": vid}) for vid in variant_ids]
        shape_results = await asyncio.gather(*shape_tasks, return_exceptions=True)

        shapes:           dict[int, list[tuple[float, float]]] = {}
        variant_headsign: dict[int, str]                       = {}

        for vid, res in zip(variant_ids, shape_results):
            if isinstance(res, Exception) or res is None:
                continue
            if "segments" not in res:
                continue
            optional_name = res.get("optionalName") or ""
            if optional_name:
                variant_headsign[vid] = optional_name
            points: list[tuple[float, float]] = []
            for seg in res["segments"]:
                nodes     = sorted(seg["nodes"], key=lambda n: n["orderNo"])
                start_idx = 1 if points else 0
                for node in nodes[start_idx:]:
                    points.append((node["latitude"], node["longitude"]))
            if points:
                shapes[vid] = points

        log.info("[%s] Kształty: %d / %d wariantów; %d nagłówków",
                 city_name, len(shapes), len(variant_ids), len(variant_headsign))

        # 4b. Uzupełnienie trip_headsign i direction_id
        for trip in trips_meta.values():
            vid = trip["variant_id"]
            if not trip["trip_headsign"]:
                trip["trip_headsign"] = variant_headsign.get(vid, "")
            trip["direction_id"] = variant_direction.get(vid, "")

        # 4c. Brakujące końcowe przystanki przez getRealCourse
        log.info("[%s] Pobieranie getRealCourse dla %d kursów ...", city_name, len(trips_meta))

        # Mapowanie symbol → symbol (stop_id = symbol w całym feedzie)
        symbol_to_stop_id: dict[str, str] = {s["symbol"]: s["symbol"] for s in stops}

        real_course_tasks   = [api.get("getRealCourse.json", {"courseId": cid}) for cid in trips_meta]
        real_course_results = await asyncio.gather(*real_course_tasks, return_exceptions=True)

        patched = 0
        for course_id, result in zip(trips_meta.keys(), real_course_results):
            if isinstance(result, Exception) or result is None:
                continue
            real_course = result.get("realCourse")
            if not real_course:
                continue
            for stopping in (real_course.get("stoppings") or []):
                order    = stopping["orderInCourse"]
                symbol   = stopping["stopPointSymbol"]
                dep_secs = stopping["scheduledDepartureSec"]
                if order in seen_orders[course_id]:
                    continue
                stop_id = symbol_to_stop_id.get(symbol)
                if stop_id is None:
                    log.debug("[%s] getRealCourse: nieznany symbol %s (kurs %s)", city_name, symbol, course_id)
                    continue
                seen_orders[course_id].add(order)
                stop_times_raw[course_id].append((order, stop_id, dep_secs, 0, 0))
                patched += 1

        log.info("[%s] Uzupełniono %d wpisów stop_times z getRealCourse", city_name, patched)

    # ── 5. Budowanie plików CSV ───────────────────────────────────────────────

    # agency.txt
    agency_fields_ordered = [
        "agency_id", "agency_name", "agency_url", "agency_timezone",
        "agency_lang", "agency_phone", "agency_fare_url", "agency_email",
    ]
    agency_present = [f for f in agency_fields_ordered if f in agency_cfg]
    agency_csv     = write_gtfs_csv([agency_cfg], agency_present)

    # stops.txt – tylko przystanki z endpointów API, które mają przynajmniej
    # jeden stop_time. Cleanup dotyczy wyłącznie danych z API – EXTRA_STOPS
    # (ręcznie dodane przystanki) są wstrzykiwane później przez inject_manual_trips().
    used_stop_ids: set[str] = {
        entry[1]
        for entries in stop_times_raw.values()
        for entry in entries
    }
    stops_rows = [
        {
            # stop_id = symbol przystanku (string) – klucz używany w stop_times.txt
            "stop_id":       s["symbol"],
            "stop_code":     s["symbol"],
            "stop_name":     s["name"],
            "stop_lat":      s["latitude"],
            "stop_lon":      s["longitude"],
            "location_type": 0,
        }
        for s in stops
        if s["symbol"] in used_stop_ids
    ]
    removed_stops = len(stops) - len(stops_rows)
    if removed_stops:
        log.info("[%s] stops.txt: usunięto %d przystanków bez stop_times", city_name, removed_stops)
    stops_csv = write_gtfs_csv(
        stops_rows,
        ["stop_id", "stop_code", "stop_name", "stop_lat", "stop_lon", "location_type"],
    )

    # routes.txt
    route_fields = ["route_id", "agency_id", "route_short_name", "route_long_name", "route_type"]
    if use_route_colors:
        route_fields += ["route_color", "route_text_color"]

    seen_route_ids: set = set()
    routes_rows = []
    for line in lines:
        if line["id"] in seen_route_ids:
            continue
        seen_route_ids.add(line["id"])
        row: dict = {
            "route_id":         line["name"],
            "agency_id":        agency_cfg["agency_id"],
            "route_short_name": line["name"],
            "route_long_name":  "",
            "route_type":       ROUTE_TYPE_MAP.get(line.get("lineType", "BUS"), 3),
        }
        if use_route_colors:
            color_pair          = route_color_lookup.get(line["name"])
            row["route_color"]      = color_pair[0] if color_pair else ""
            row["route_text_color"] = color_pair[1] if color_pair else ""
        routes_rows.append(row)
    routes_csv = write_gtfs_csv(routes_rows, route_fields)

    # calendar_dates.txt
    cal_rows = [
        {"service_id": sid, "date": date_str, "exception_type": 1}
        for sid, dates in service_dates.items()
        for date_str in dates
    ]
    cal_rows.sort(key=lambda r: (r["date"], r["service_id"]))
    cal_csv = write_gtfs_csv(cal_rows, ["service_id", "date", "exception_type"])

    # trips.txt
    trips_rows = []
    for t in trips_meta.values():
        brigade    = t["brigade"]
        service_id = t["service_id"]
        block_id   = block_id_lookup.get((service_id, brigade), brigade) if brigade else ""
        trips_rows.append({
            "route_id":      t["route_id"],
            "service_id":    service_id,
            "trip_id":       t["trip_id"],
            "trip_headsign": t["trip_headsign"],
            "direction_id":  t.get("direction_id", ""),
            "shape_id":      t["shape_id"],
            "block_id":      block_id,
        })
    trips_csv = write_gtfs_csv(
        trips_rows,
        ["route_id", "service_id", "trip_id", "trip_headsign", "direction_id", "shape_id", "block_id"],
    )

    # stop_times.txt
    _DAY = 86400
    st_rows = []
    for course_id, entries in stop_times_raw.items():
        sorted_entries = sorted(entries)
        offset   = 0
        prev_raw = None
        for order, stop_id, dep_secs, pickup_type, dropoff_type in sorted_entries:
            if prev_raw is not None and dep_secs < prev_raw:
                offset += _DAY
            prev_raw = dep_secs
            time_str = secs_to_hhmmss(dep_secs + offset)
            st_rows.append({
                "trip_id":        course_id,
                "arrival_time":   time_str,
                "departure_time": time_str,
                "stop_id":        stop_id,
                "stop_sequence":  order,
                "pickup_type":    pickup_type,
                "drop_off_type":  dropoff_type,
            })
    st_rows.sort(key=lambda r: (r["trip_id"], r["stop_sequence"]))
    st_csv = write_gtfs_csv(
        st_rows,
        ["trip_id", "arrival_time", "departure_time",
         "stop_id", "stop_sequence", "pickup_type", "drop_off_type"],
    )

    # shapes.txt
    shapes_rows = [
        {"shape_id": sid, "shape_pt_lat": lat, "shape_pt_lon": lon, "shape_pt_sequence": seq}
        for sid, points in shapes.items()
        for seq, (lat, lon) in enumerate(points)
    ]
    shapes_csv = write_gtfs_csv(
        shapes_rows,
        ["shape_id", "shape_pt_lat", "shape_pt_lon", "shape_pt_sequence"],
    )

    # feed_info.txt
    feed_row = {
        "feed_publisher_name": feed_cfg["feed_publisher_name"],
        "feed_publisher_url":  feed_cfg["feed_publisher_url"],
        "feed_lang":           feed_cfg.get("feed_lang", "pl"),
        "feed_timezone":       feed_cfg.get("feed_timezone", "Europe/Warsaw"),
        "feed_start_date":     feed_start,
        "feed_end_date":       feed_end,
        "feed_version":        feed_version,
    }
    feed_csv = write_gtfs_csv([feed_row], list(feed_row.keys()))

    # ── 6. ZIP ───────────────────────────────────────────────────────────────
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("agency.txt",         agency_csv)
        zf.writestr("stops.txt",          stops_csv)
        zf.writestr("routes.txt",         routes_csv)
        zf.writestr("calendar_dates.txt", cal_csv)
        zf.writestr("trips.txt",          trips_csv)
        zf.writestr("stop_times.txt",     st_csv)
        zf.writestr("shapes.txt",         shapes_csv)
        zf.writestr("feed_info.txt",      feed_csv)

    size = buf.tell()
    log.info("[%s] Generowanie ukończone – ZIP: %s bajtów (%.1f MB)",
             city_name, f"{size:,}", size / 1_048_576)
    return buf.getvalue()


