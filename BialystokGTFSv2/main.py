# ══════════════════════════════════════════════════════════════════════════════
#  RUNNER – GENEROWANIE STATYCZNE + INICJALIZACJA REALTIME
# ══════════════════════════════════════════════════════════════════════════════

# Słownik: city_name → RealtimeState (wypełniony dla miast z realtime=True)
_rt_states: dict[str, RealtimeState] = {}
_rt_dirs_for_http: dict[str, str] = {}


async def run_city(city_cfg: dict) -> None:
    """
    Generuje GTFS-Static, wstrzykuje ręczne kursy, zapisuje ZIP, ładuje dane
    do RealtimeState.

    Kolejność kroków jest celowa i gwarantowana:
      1. generate_city()       – dane z API + cleanup nieużywanych przystanków
      2. inject_manual_trips() – DOPIERO TERAZ dodaje EXTRA_STOPS i MANUAL_TRIPS
                                 do oczyszczonego ZIP
      3. Zapis ZIP na dysk i załadowanie do RealtimeState
    """
    city_name = city_cfg["name"]
    try:
        # Krok 1: generowanie i cleanup (tylko dane z API)
        zip_bytes = await generate_city(city_cfg)

        # Krok 2: wstrzyknięcie ręcznych danych PO cleanup'ie.
        # Uruchamiamy w executor, żeby nie blokować pętli zdarzeń
        # podczas synchronicznych zapytań HTTP do OSRM.
        loop = asyncio.get_running_loop()
        zip_bytes = await loop.run_in_executor(None, inject_manual_trips, zip_bytes, city_name)

        # Krok 3: zapis i załadowanie do RealtimeState
        os.makedirs(OUTPUT_DIR, exist_ok=True)
        path = static_path(city_name)
        with open(path, "wb") as fh:
            fh.write(zip_bytes)
        log.info("[%s] Static ZIP zapisany → %s", city_name, path)

        # Ładowanie do pamięci dla GTFS-RT (jeśli włączone).
        # load_from_zip buduje bloki bezpośrednio z ZIP – nie potrzeba zewnętrznego API.
        if city_cfg.get("realtime") and city_name in _rt_states:
            state = _rt_states[city_name]
            await loop.run_in_executor(None, state.load_from_zip, zip_bytes)

    except Exception:
        log.exception("[%s] Generowanie GTFS-Static nie powiodło się", city_name)


async def run_all() -> None:
    await asyncio.gather(*[run_city(cfg) for cfg in CITIES])


